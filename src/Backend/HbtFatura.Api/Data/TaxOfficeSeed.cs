    public static async Task SeedTaxOfficesIfEmptyAsync(AppDbContext db, CancellationToken ct = default)
    {
        if (await db.TaxOffices.AnyAsync(ct))
            return;

        var cities = await db.Cities.Include(x => x.Districts).ToListAsync(ct);
        var now = DateTime.UtcNow;
        var toAdd = new List<TaxOffice>();

        var allEntries = TaxOfficeEntriesPart1.Concat(TaxOfficeEntriesPart2);

        foreach (var entry in allEntries)
        {
            var city = cities.FirstOrDefault(x => x.Name.Equals(entry.City, StringComparison.OrdinalIgnoreCase));
            if (city == null) continue;

            var district = city.Districts.FirstOrDefault(x => x.Name.Equals(entry.District, StringComparison.OrdinalIgnoreCase));
            if (district == null) continue;

            toAdd.Add(new TaxOffice
            {
                Id = Guid.NewGuid(),
                CityId = city.Id,
                DistrictId = district.Id,
                Name = entry.Name,
                CreatedAt = now
            });
        }

        if (toAdd.Count > 0)
        {
            await db.TaxOffices.AddRangeAsync(toAdd, ct);
            await db.SaveChangesAsync(ct);
        }
    }
