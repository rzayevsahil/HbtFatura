    public static async Task SeedIfEmptyAsync(AppDbContext db, CancellationToken ct = default)
    {
        if (await db.MaterialIconOptions.AnyAsync(ct))
            return;

        var order = 0;
        foreach (var name in DefaultLigatures)
        {
            db.MaterialIconOptions.Add(new MaterialIconOption
            {
                Id = Guid.NewGuid(),
                LigatureName = name,
                SortOrder = order++,
                IsActive = true,
                CreatedAtUtc = DateTime.UtcNow
            });
        }

        await db.SaveChangesAsync(ct);
    }
